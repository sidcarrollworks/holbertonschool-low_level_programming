#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: num1
 * @b: num2
 * Return: return the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts
 * @a: num1
 * @b: num2
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies
 * @a: num1
 * @b: num2
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: num1
 * @b: num2
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: num1
 * @b: num2
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
